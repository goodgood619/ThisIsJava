public class Fruit {
    private String fruit;
    private int price;
    public Fruit(String fruit,int price) {
        this.fruit = fruit;
        this.price = price;
    }

    public int getPrice() {
        return price;
    }

    public String getFruit() {
        return fruit;
    }
}
